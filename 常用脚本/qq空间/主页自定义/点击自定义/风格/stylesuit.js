	//计算QZONE.shop.suit.add所需要的参数对象
	function getShopItem(obj){
		obj.posx=obj.posy=obj.width=obj.height=obj.zindex=0;
		switch(obj.Ftype_id){
			case '1'://skin
				var desc=obj.Fdesc;
				var pos=desc.indexOf("@");
                
                if (pos != -1) {
                    var contype = desc.substr(0, pos);
                    if (contype == 1) {
                        obj.posx = 175;
                    }
                    desc = desc.substring(pos + 1);
                }
                if (0 == obj.Fitem_mode) {
                    obj.zindex = 1 << 3;
                    if ('jpg' == desc) {
                        obj.zindex += 1;
                    }
                }
                else 
                    if (1 == obj.Fitem_mode) {
                        var m = desc.split("|");
                        if (2 == m.length) {
                            var m1 = m[0].split("_");
                            if (3 == m1.length) {
                                obj.posy = m1[0];
                                obj.zindex += (m1[1] << 5);
                                if ('jpg' == m1[2]) {
                                    obj.zindex += 1 << 1;
                                }
                            }
                            if ('jpg' == m[1]) {
                                obj.zindex += 1 << 2;
                            }
                            obj.zindex += 1 << 4;
                        }
                    }
                    else 
                        if (2 == obj.Fitem_mode) {
                            var m = desc.split("|");
                            if (3 == m.length) {
                                if ('jpg' == m[0]) {
                                    obj.zindex += 1;
                                }
                                var m1 = m[1].split("_");
                                if (3 == m1.length) {
                                    obj.posy = m1[0];
                                    obj.zindex += (m1[1] << 5);
                                    if ('jpg' == m1[2]) {
                                        obj.zindex += 1 << 1;
                                    }
                                }
                                if ('jpg' == m[2]) {
                                    obj.zindex += 1 << 2;
                                }
                                obj.zindex += 1 << 3;
                                obj.zindex += 1 << 4;
                            }
                        }
                        else 
                            if (5 == obj.Fitem_mode) {
                                var m = desc.split("|");
                                if (3 == m.length) {
                                    if ('jpg' == m[0]) {
                                        obj.zindex += 1;
                                    }
                                    var m1 = m[1].split("_");
                                    if (3 == m1.length) {
                                        obj.posy = m1[0];
                                        obj.zindex += (m1[1] << 5);
                                        if ('jpg' == m1[2]) {
                                            obj.zindex += 1 << 1;
                                        }
                                    }
                                    if ('jpg' == m[2]) {
                                        obj.zindex += 1 << 2;
                                    }
                                    obj.zindex += 1 << 3;
                                    obj.zindex += 1 << 4;
                                }
                            }
				break;
			case '5'://float
				var m=obj.Fdesc.split("|");if(2==m.length){obj.posy=m[0];obj.width=m[1];}
				break;
			case '7'://flower
				obj.level=obj.Fvip_price;
				break;
			case '11'://big	
				obj.zindex=obj.Fdesc;
				break;
			case '13'://自定义菜单
				obj.zindex=obj.Fdesc;
				break;
			case '19'://标题栏
				var desc=obj.Fdesc+"";
				if(desc.indexOf("gif")!=-1){obj.zindex=0;}
				if(desc.indexOf("jpg")!=-1){obj.zindex=1;}
				if(desc.indexOf("png")!=-1){obj.zindex=2;}
				if(parseInt(desc)>100){obj.zindex+=4;obj.posy=parseInt(desc);}
				var pw=0;if((pw=desc.indexOf('|'))!=-1){obj.width=parseInt(desc.substr(pw+1));}
				break;
		}

        
        if (typeof(obj.Ftype_id) == 'string') {
            obj.Ftype_id_x = parseInt(obj.Ftype_id);
        }
        else {
            obj.Ftype_id_x = obj.Ftype_id;
        }
        if (typeof(obj.Fitem_id) == 'string') {
            obj.Fitem_id_x = parseInt(obj.Fitem_id);
        }
        else {
            obj.Fitem_id_x = obj.Fitem_id;
        }
        if (typeof(obj.posx) == 'string') {
            obj.posx_x = parseInt(obj.posx);
        }
        else {
            obj.posx_x = obj.posx;
        }
        if (typeof(obj.posy) == 'string') {
            obj.posy_x = parseInt(obj.posy);
        }
        else {
            obj.posy_x = obj.posy;
        }
        if (typeof(obj.width) == 'string') {
            obj.width_x = parseInt(obj.width);
        }
        else {
            obj.width_x = obj.width;
        }
        if (typeof(obj.height) == 'string') {
            obj.height_x = parseInt(obj.height);
        }
        else {
            obj.height_x = obj.height;
        }
        if (typeof(obj.zindex) == 'string') {
            obj.zindex_x = parseInt(obj.zindex);
        }
        else {
            obj.zindex_x = obj.zindex;
        }
        return {
            'type': obj.Ftype_id_x,
            'itemno': obj.Fitem_id_x,
            'posx': obj.posx_x,
            'posy': obj.posy_x,
            'posz': 0,
            'width': obj.width_x,
            'height': obj.height_x,
            'flag': obj.zindex_x
        };
	}


	//删除数据, n表示第几项, 从0开始算起。
	function delArray(array, n) { 				
	    if(!array || array.length<n+1 || n <0){
			return array;
	    }else{
			var temp = [];
			for(var i=0; i<array.length; i++){
				if(i!=0){ temp.push(array[i]); }
			}
			return temp;
	    }		
	}
