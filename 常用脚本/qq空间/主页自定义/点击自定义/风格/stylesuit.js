	var onbut_flag = false;
	QZONE.shop.styleSuit.add = function(style, id){		
		if(onbut_flag){return;} onbut_flag = true;

		var suit = getSuitItem(QZONE.shop.styleSuit.htType[style], id);
		if(checkSuitList(suit.Fitem_id)){
			addSavedAll();
		} else {			
			for(var i=0; i<suit.suitItems.length; i++){
				QZONE.shop.add(getShopItem(suit.suitItems[i]));
			}
			removeItems(suit.suitItems);
		}

		onbut_flag = false;
		return false;
	};

	//删除当前物品列表中不包含的所有物品
	function removeItems(suitList){ 
		var tmp = [];
		for(var i=0; i<suitList.length; i++){
			tmp.push(suitList[i].Ftype_id+'_'+suitList[i].Fitem_id);
		}
		tmp.push(QZONE.shop.shopLib.getSavedItemList());
		var itemList = tmp.join('|');

		var citems = QZONE.shop.shopLib.getItemList().split('|');
		for(var i=0; i<citems.length; i++){
			var citem = citems[i].split('_');
			if(itemList.indexOf(citem[0]+'_'+citem[1])<0){
				for(var j=0; j<citem.length; j++){
					citem[j] = parseInt(citem[j]);
				}
				QZONE.shop.remove({'type':citem[0], 'itemno':citem[1], 'posx':citem[2], 'posy':citem[3], 'posz':citem[4], 'width':citem[5], 'height':citem[6], 'flag':citem[7]});
			}
		}		
	}

